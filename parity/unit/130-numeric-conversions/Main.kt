fun main() {
    val i = 7
    val l = i.toLong()
    val d = i.toDouble()
    val f = i.toFloat()
    val s = i.toString()
    println(l)
    println(d)
    println(f)
    println(s)

    val d2 = 3.14
    println(d2.toInt())
    println(d2.toLong())

    val str = "42"
    println(str.toInt())
    println(str.toLong())
}
