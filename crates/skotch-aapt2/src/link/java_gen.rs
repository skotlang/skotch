//! R.java / proguard / text-symbol generation.
//!
//! Port of `java/JavaClassGenerator.{h,cpp}`, `java/ProguardRules.cpp`
//! (collection subset), and the R.txt writer.

use super::LinkContext;
use crate::diag::Diagnostics;
use crate::res::table::ResourceTable;
use crate::res::value::{Styleable, ValueKind};
use crate::res::{ResourceType, Source};
use crate::xml::Element;
use anyhow::Result;
use std::fmt::Write as _;

#[derive(Debug, Clone, Default)]
pub struct JavaGenOptions {
    pub custom_package: Option<String>,
    pub extra_packages: Vec<String>,
    pub javadoc_annotations: Vec<String>,
    pub private_symbols: Option<String>,
    /// `--non-final-ids` (implied by `--static-lib`).
    pub non_final_ids: bool,
    pub rename_manifest_package: Option<String>,
}

const HEADER: &str = "/* AUTO-GENERATED FILE. DO NOT MODIFY.\n *\n * This class was automatically generated by the\n * aapt tool from the resource data it found. It\n * should not be modified by hand.\n */\n\n";

/// Transforms a resource entry name into a Java field name (`.` and
/// `-` become `_`). Mirrors `JavaClassGenerator::TransformToFieldName`.
pub fn to_field_name(name: &str) -> String {
    name.replace(['.', '-'], "_")
}

/// Generates the R classes: the main one plus copies for
/// `--extra-packages`. Returns (java package, source) pairs.
pub fn generate_java_classes(
    table: &ResourceTable,
    _manifest: &Element,
    context: &LinkContext,
    options: &JavaGenOptions,
    _diag: &Diagnostics,
) -> Result<Vec<(String, String)>> {
    let main_package = options
        .custom_package
        .clone()
        .unwrap_or_else(|| context.compilation_package.clone());

    let source = generate_r_java(table, &main_package, options);
    let mut out = vec![(main_package, source.clone())];
    for extra in &options.extra_packages {
        for package in extra.split(':').filter(|p| !p.is_empty()) {
            let copy = regenerate_package_line(&source, package);
            out.push((package.to_string(), copy));
        }
    }
    Ok(out)
}

fn regenerate_package_line(source: &str, package: &str) -> String {
    let mut out = String::with_capacity(source.len());
    for line in source.lines() {
        if let Some(rest) = line.strip_prefix("package ") {
            let _ = rest;
            out.push_str(&format!("package {package};"));
        } else {
            out.push_str(line);
        }
        out.push('\n');
    }
    out
}

/// Generates one R.java for `java_package`.
pub fn generate_r_java(
    table: &ResourceTable,
    java_package: &str,
    options: &JavaGenOptions,
) -> String {
    let finality = if options.non_final_ids { "" } else { "final " };
    let mut out = String::new();
    out.push_str(HEADER);
    let _ = writeln!(out, "package {java_package};");
    out.push('\n');
    for annotation in &options.javadoc_annotations {
        let _ = writeln!(out, "/** {annotation} */");
    }
    out.push_str("public final class R {\n");

    for (_, types) in table.sorted_view() {
        // Group named types by their Java class name (custom-named
        // types like `string.v2` collapse into `string`).
        let mut current_class: Option<String> = None;
        for (ty, entries) in types {
            let class_name = ty.named_type.ty.as_str().replace('^', "");
            if current_class.as_deref() != Some(&class_name) {
                if current_class.is_some() {
                    out.push_str("  }\n");
                }
                let _ = writeln!(out, "  public static final class {class_name} {{");
                current_class = Some(class_name.clone());
            }

            if ty.named_type.ty == ResourceType::Styleable {
                for entry in entries {
                    if let Some(ValueKind::Styleable(styleable)) = entry
                        .values
                        .first()
                        .and_then(|v| v.value.as_ref())
                        .map(|v| &v.kind)
                    {
                        write_styleable(&mut out, &entry.name, styleable, finality);
                    }
                }
            } else {
                for entry in entries {
                    let Some(id) = entry.id else { continue };
                    let _ = writeln!(
                        out,
                        "    public static {finality}int {}=0x{:08x};",
                        to_field_name(&entry.name),
                        id.0
                    );
                }
            }
        }
        if current_class.is_some() {
            out.push_str("  }\n");
        }
    }
    out.push_str("}");
    out.push('\n');
    out
}

fn write_styleable(out: &mut String, name: &str, styleable: &Styleable, finality: &str) {
    // Sort attribute references by resource ID, mirroring the
    // JavaClassGenerator's sorted styleable layout.
    let mut sorted: Vec<_> = styleable.entries.iter().collect();
    sorted.sort_by_key(|reference| reference.id.map(|id| id.0).unwrap_or(0));

    let field_name = to_field_name(name);
    let ids: Vec<String> = sorted
        .iter()
        .map(|r| format!("0x{:08x}", r.id.map(|id| id.0).unwrap_or(0)))
        .collect();
    let _ = writeln!(
        out,
        "    public static {finality}int[] {field_name}={{\n      {}\n    }};",
        ids.join(", ")
    );
    for (index, reference) in sorted.iter().enumerate() {
        let Some(attr_name) = &reference.name else {
            continue;
        };
        let mut suffix = String::new();
        if !attr_name.package.is_empty() {
            suffix.push_str(&attr_name.package.replace('.', "_"));
            suffix.push('_');
        }
        suffix.push_str(&to_field_name(&attr_name.entry));
        let _ = writeln!(
            out,
            "    public static {finality}int {field_name}_{suffix}={index};"
        );
    }
}

/// Generates the R.txt symbol list (`--output-text-symbols`).
pub fn generate_text_symbols(table: &ResourceTable) -> String {
    let mut out = String::new();
    for (_, types) in table.sorted_view() {
        for (ty, entries) in types {
            for entry in entries {
                if ty.named_type.ty == ResourceType::Styleable {
                    let _ = writeln!(out, "int[] styleable {} {{ }}", to_field_name(&entry.name));
                    if let Some(ValueKind::Styleable(styleable)) = entry
                        .values
                        .first()
                        .and_then(|v| v.value.as_ref())
                        .map(|v| &v.kind)
                    {
                        let mut sorted: Vec<_> = styleable.entries.iter().collect();
                        sorted.sort_by_key(|r| r.id.map(|id| id.0).unwrap_or(0));
                        for (index, reference) in sorted.iter().enumerate() {
                            if let Some(name) = &reference.name {
                                let mut suffix = String::new();
                                if !name.package.is_empty() {
                                    suffix.push_str(&name.package.replace('.', "_"));
                                    suffix.push('_');
                                }
                                suffix.push_str(&to_field_name(&name.entry));
                                let _ = writeln!(
                                    out,
                                    "int styleable {}_{suffix} {index}",
                                    to_field_name(&entry.name)
                                );
                            }
                        }
                    }
                } else if let Some(id) = entry.id {
                    let _ = writeln!(
                        out,
                        "int {} {} 0x{:08x}",
                        ty.named_type.ty.as_str().replace('^', ""),
                        to_field_name(&entry.name),
                        id.0
                    );
                }
            }
        }
    }
    out
}

