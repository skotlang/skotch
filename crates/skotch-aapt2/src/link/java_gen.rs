/// A class to keep, with the source it was referenced from.
#[derive(Debug, Default)]
pub struct KeepSet {
    pub conditional: bool,
    /// (usage tag, class name, source).
    classes: Vec<(String, String, Source)>,
    /// (class, method signature, source).
    methods: Vec<(String, String, Source)>,
}

impl KeepSet {
    pub fn new(conditional: bool) -> Self {
        KeepSet {
            conditional,
            ..Default::default()
        }
    }

    pub fn add_class(&mut self, usage: &str, class: &str, source: Source) {
        self.classes
            .push((usage.to_string(), class.to_string(), source));
    }

    pub fn add_method(&mut self, class: &str, signature: &str, source: Source) {
        self.methods
            .push((class.to_string(), signature.to_string(), source));
    }

    /// Collects keep rules from a linked XML document (layouts:
    /// custom views; manifest: components). Mirrors the collectors in
    /// `java/ProguardRules.cpp`.
    pub fn collect_from_xml(&mut self, element: &Element, source_path: &str, kind: XmlKind) {
        let source = Source::with_line(source_path, element.line_number);
        match kind {
            XmlKind::Manifest => {
                if matches!(
                    element.name.as_str(),
                    "activity"
                        | "service"
                        | "receiver"
                        | "provider"
                        | "application"
                        | "instrumentation"
                        | "activity-alias"
                ) {
                    if let Some(name) = element.attr_value(crate::xml::SCHEMA_ANDROID, "name") {
                        self.add_class("AndroidManifest.xml", name, source.clone());
                    }
                    if let Some(backup_agent) =
                        element.attr_value(crate::xml::SCHEMA_ANDROID, "backupAgent")
                    {
                        self.add_class("AndroidManifest.xml", backup_agent, source.clone());
                    }
                    if let Some(app_component) =
                        element.attr_value(crate::xml::SCHEMA_ANDROID, "appComponentFactory")
                    {
                        self.add_class("AndroidManifest.xml", app_component, source.clone());
                    }
                    if let Some(zygote) =
                        element.attr_value(crate::xml::SCHEMA_ANDROID, "zygotePreloadName")
                    {
                        self.add_class("AndroidManifest.xml", zygote, source.clone());
                    }
                }
            }
            XmlKind::Layout => {
                // Custom views: element names containing '.'.
                if element.name.contains('.') {
                    self.add_class("layout", &element.name, source.clone());
                }
                if element.name == "view" || element.name == "fragment" {
                    if let Some(class) = element
                        .attr_value("", "class")
                        .or_else(|| element.attr_value(crate::xml::SCHEMA_ANDROID, "name"))
                    {
                        self.add_class("layout", class, source.clone());
                    }
                }
                if let Some(handler) = element.attr_value(crate::xml::SCHEMA_ANDROID, "onClick") {
                    self.add_method(
                        "android.content.Context",
                        &format!("void {handler}(android.view.View)"),
                        source.clone(),
                    );
                }
            }
            XmlKind::Other => {
                if element.name.contains('.') {
                    self.add_class("xml", &element.name, source.clone());
                }
            }
        }
        for child in element.child_elements() {
            self.collect_from_xml(child, source_path, kind);
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum XmlKind {
    Manifest,
    Layout,
    Other,
}

/// Renders the keep rules. Mirrors `proguard::WriteKeepSet`.
pub fn generate_proguard_rules(keep_set: &KeepSet, no_location_reference: bool) -> String {
    let mut out = String::new();
    let mut seen = std::collections::BTreeSet::new();
    for (usage, class, source) in &keep_set.classes {
        if !seen.insert(class.clone()) {
            continue;
        }
        if !no_location_reference {
            let _ = writeln!(out, "# Referenced at {source}");
            let _ = writeln!(out, "# {usage}");
        }
        let _ = writeln!(out, "-keep class {class} {{ <init>(...); }}");
    }
    let mut seen_methods = std::collections::BTreeSet::new();
    for (class, signature, source) in &keep_set.methods {
        if !seen_methods.insert((class.clone(), signature.clone())) {
            continue;
        }
        if !no_location_reference {
            let _ = writeln!(out, "# Referenced at {source}");
        }
        let _ = writeln!(out, "-keepclassmembers class * {{ *** {signature}; }}");
    }
    out
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::res::config::ConfigDescription;
    use crate::res::value::{Item, Value};
    use crate::res::{ResourceId, ResourceName};

    #[test]
    fn r_java_basic() {
        let mut table = ResourceTable::new();
        table
            .add_value(
                ResourceName::new("com.app", ResourceType::String, "app.name"),
                ConfigDescription::default(),
                Value::item(Item::String {
                    value: "x".to_string(),
                    untranslatable_sections: vec![],
                }),
            )
            .unwrap();
        table
            .find_resource_mut(&ResourceName::new(
                "com.app",
                ResourceType::String,
                "app.name",
            ))
            .unwrap()
            .id = Some(ResourceId(0x7f020000));

        let source = generate_r_java(&table, "com.app", &JavaGenOptions::default());
        assert!(source.contains("package com.app;"), "{source}");
        assert!(
            source.contains("public static final class string"),
            "{source}"
        );
        assert!(
            source.contains("public static final int app_name=0x7f020000;"),
            "{source}"
        );
    }

    #[test]
    fn non_final_ids() {
        let mut table = ResourceTable::new();
        table
            .add_value(
                ResourceName::new("com.app", ResourceType::Id, "x"),
                ConfigDescription::default(),
                Value::item(Item::Id),
            )
            .unwrap();
        table
            .find_resource_mut(&ResourceName::new("com.app", ResourceType::Id, "x"))
            .unwrap()
            .id = Some(ResourceId(0x7f010000));
        let options = JavaGenOptions {
            non_final_ids: true,
            ..Default::default()
        };
        let source = generate_r_java(&table, "com.app", &options);
        assert!(
            source.contains("public static int x=0x7f010000;"),
            "{source}"
        );
    }
}
